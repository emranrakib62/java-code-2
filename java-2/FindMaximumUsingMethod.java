
public class FindMaximumUsingMethod {
    public static void main(String[] args) {
        
        int num1 = 10;
        int num2 = 20;
        int max = findMaximum(num1, num2);
        System.out.println("The maximum number is: " + max);
    }

    public static int findMaximum(int a, int b) {
        return Math.max(a, b);
    }
}

    
        
    
        
        
        
        
 